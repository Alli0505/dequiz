export function t(lang, key) {
  return (UI[lang] && UI[lang][key]) ?? UI.en[key] ?? key;
}

// Fill {tokens}; {s} is a plural suffix helper (English/Russian style handled by caller).
export function fill(str, vars = {}) {
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? vars[k] : ''));
}

